# Jumlah total kue
total_kue = 24

# Jumlah anak
jumlah_anak = 5

# Menghitung jumlah kue per anak
kue_per_anak = total_kue // jumlah_anak

# Menghitung sisa kue
sisa_kue = total_kue % jumlah_anak

# Mencetak hasil
print("Setiap anak mendapat", kue_per_anak, "kue")
print("Sisa kue yang tidak terbagi adalah", sisa_kue, "kue")